// Optional local image overrides:
// Drop files in src/assets/profiles/<profile_key>/card.(png|jpg|jpeg|webp)
// and src/assets/profiles/<profile_key>/avatar.(png|jpg|jpeg|webp)
// Example: src/assets/profiles/female_001/card.jpg
const CUSTOM_FULL_IMAGE_MODULES = import.meta.glob(
  '../assets/profiles/*/card.{png,jpg,jpeg,webp}',
  { eager: true, import: 'default' }
) as Record<string, string>;
const CUSTOM_AVATAR_IMAGE_MODULES = import.meta.glob(
  '../assets/profiles/*/avatar.{png,jpg,jpeg,webp}',
  { eager: true, import: 'default' }
) as Record<string, string>;

const extractProfileKeyFromPath = (path: string): string | null => {
  const match = path.match(/\/profiles\/([^/]+)\//);
  return match?.[1] ?? null;
};

const CUSTOM_IMAGES: Record<string, { full?: string; avatar?: string }> = {};

Object.entries(CUSTOM_FULL_IMAGE_MODULES).forEach(([path, imageUrl]) => {
  const key = extractProfileKeyFromPath(path);
  if (!key) return;
  CUSTOM_IMAGES[key] = { ...(CUSTOM_IMAGES[key] ?? {}), full: imageUrl };
});

Object.entries(CUSTOM_AVATAR_IMAGE_MODULES).forEach(([path, imageUrl]) => {
  const key = extractProfileKeyFromPath(path);
  if (!key) return;
  CUSTOM_IMAGES[key] = { ...(CUSTOM_IMAGES[key] ?? {}), avatar: imageUrl };
});

const resolveImagesForProfile = (profileKey: string): { full: string; avatar: string } => {
  const custom = CUSTOM_IMAGES[profileKey];
  if (custom?.full && custom?.avatar) {
    return { full: custom.full, avatar: custom.avatar };
  }
  const figmaLinked = FIGMA_LINK_IMAGES[profileKey];
  if (figmaLinked?.full && figmaLinked?.avatar) {
    return figmaLinked;
  }
  return IMAGES[profileKey] || IMAGES['female_001'];
};

